# Conjuntos

my_set1 = {1, 2, 3, 4}
my_set2 = {3, 4, 5, 6}

# .add()
# .remove()
# .discard()
# .pop()

# .union()
my_set3 = my_set1.union(my_set2)

# .intersection()
my_set4 = my_set1.intersection(my_set2)

# .difference()
my_set5 = my_set1.difference(my_set2)

# .symmetric_difference
my_set6 = my_set1.symmetric_difference(my_set2)

# .issubset()
my_set7 = my_set1.issubset(my_set2)

# .issuperset()
my_set8 = my_set1.issuperset(my_set2)

print(my_set8)
